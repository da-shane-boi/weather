    def test_hour_details(self):
        api = Api(True)
        api.KEY = None
        api.response = self.FORECAST_DAY
        params = handler.generate_command_paramaters(main.create_parser(['--hour', '10']))
        with captured_io(StringIO()) as (out, err):
            handler.handle_commands(api, params)
        output = out.getvalue().lower()
        self.assertTrue("10:00" in output)
        self.assertTrue("cape town" in output)
        self.assertTrue("condition" in output and "sunny" in output)
        self.assertTrue("temperature" in output and "21.6" in output)
        self.assertTrue("humidity" in output and "68%" in output)
        self.assertTrue("chance of rain" in output and "0%" in output)
        self.assertTrue("chance of snow" in output and "0%" in output)
        self.assertTrue("wind" in output)
        self.assertTrue("uv index" in output and "high" in output)


